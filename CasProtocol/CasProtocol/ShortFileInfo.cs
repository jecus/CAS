namespace CasProtocol
{
    using System;

    /// <summary>
    /// Класс, коротко описывает файл 
    /// </summary>
    internal class ShortFileInfo
    {
        /// <summary>
        /// Время создания файла
        /// </summary>
        public DateTime CreationTime;
        /// <summary>
        /// Название файла
        /// </summary>
        public string FileName;
        /// <summary>
        /// Размер файла
        /// </summary>
        public int FileSize;
    }
}

